        <h2 class="text-center">Sobre</h2>
        <p class="text-justify">
            O Sistema de Apoio à Gestão Contábil do CBMSC idealizado pelo Cel. Vieira (Cmte do 7º BBM), tendo como coordenador o Cap. Ananias e desenvolvedor técnico Cb Neto,  tem como objetivos: 
        </p>
        <ul class="list-group">
            <li class="">Padronização do planejamento e fatos contábeis das OBMs</li>
            <li class="">Informações instantâneas de apoio à tomada de decisão</li>
            <li class="">Automação de processos</li>
            <li class="">Transparência administrativa</li>
        </ul>
        <p class="text-justify">
            O sistema é baseado na hierarquia organizacional do CBMSC (Batalhões, Companhias, Pelotões…) dinamicamente cadastrados e modificáveis. Os níveis de acesso são baseados em funções do usuário (usuários, gerentes, administradores…) em cada OBM.
        </p>
    <p class="text-justify">
        O código está sendo desenvolvido em PHP, utilizando o framework CodeIgniter, de maneira modular, facilitando a manutenção e a continuidade do sistema.
    </p>
        
